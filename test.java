// Simple Java Program
public class Main {
    public static void main(String[] args) {
        // Print a greeting
        System.out.println("Hello, World!");

        // Declare two numbers
        int number1 = 10;
        int number2 = 20;

        // Calculate the sum
        int sum = number1 + number2;

        // Print the result
        System.out.println("The sum of " + number1 + " and " + number2 + " is: " + sum);
    }
}
